public class Vehicle {
    private String make;
    private String model;
    private String capcacity;
    private String mpg;
    private String trans;
    private boolean avalible;

    public Vehicle(String make, String model, String capacity, String mpg, String trans, boolean avalible)
    {
        this.make = make;
        this.model = model;
        this.capcacity = capacity;
        this.mpg = mpg;
        this.trans = trans;
        this.avalible = avalible;
    }

}
